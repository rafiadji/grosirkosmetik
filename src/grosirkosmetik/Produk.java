    private void BHapusProdukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BHapusProdukActionPerformed
        if(id != null){
        String[] where ={"ID_PRODUK", id};
        int tny = JOptionPane.showConfirmDialog(null, "Yakin menghapus data ini ?");
            if(tny == 0){
                boolean delete = conn.delete("produk", where);
                if(delete){
                    id = null;
                    loadTable();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Gagal menghapus !");
                }    
            }
        }
    }//GEN-LAST:event_BHapusProdukActionPerformed


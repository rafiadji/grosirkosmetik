    public void loadTable(){
        Object[] field = {"Produk","Supplier", "Harga Kulak", "Harga", "Stok"};
        TablePro = new DefaultTableModel(null, field);
        TableProduk.setModel(TablePro);
        LTambahStok.setVisible(false);
        TTambahStok.setVisible(false);
        BAdd.setVisible(false);
        try{
            ResultSet tpro = conn.select("SELECT * FROM produk");
            while(tpro.next()){
                String produk = tpro.getString("PRODUK");
                String id_supplier = tpro.getString("ID_SUPPLIER");
                String harga_kulak = tpro.getString("HARGA_KULAK");
                String harga = tpro.getString("HARGA");
                String stok = tpro.getString("STOK");
                String[] data = {produk, id_supplier,harga_kulak,harga,stok};
                TablePro.addRow(data);
            }
        }
        catch(Exception e){
            System.out.println(e);
        }
    }


    private void BAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAddActionPerformed
        // TODO add your handling code here:
        try{
            ResultSet tpro = conn.select("SELECT STOK FROM produk WHERE ID_PRODUK = '"+id+"'");
            int stok = 0;
            if(tpro.next()){
                stok = tpro.getInt("STOK");
            }
            stok = stok + Integer.parseInt(TTambahStok.getText()); 
            String[][] data = {
                {"STOK", ""+stok}

            };
            String[] where = {"ID_PRODUK", id};
            boolean update = conn.update("produk", data, where);
            if (update) {
                loadTable();
            }
            else{
                JOptionPane.showConfirmDialog(null, "Gagal Menambah Stok");
            }  
        }
        catch(Exception e){
            
        }
        
    }//GEN-LAST:event_BAddActionPerformed


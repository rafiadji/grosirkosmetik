        String getData = (TableSupplier.getModel().getValueAt(row, 0).toString());
        try{
            ResultSet tsup = conn.select("SELECT * FROM supplier WHERE ID_SUPPLIER = "+getData);
            while(tsup.next()){
                LSupVal.setText(tsup.getString("ID_SUPPLIER"));
            }  
        }
        catch(Exception e){
            System.out.println(e);
            System.exit(0);
        }

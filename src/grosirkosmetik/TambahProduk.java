    public void loadTable(){
        Object[] field = {"ID Supplier","Nama Supplier"};
        TableSupp = new DefaultTableModel(null, field);
        TableSupplier.setModel(TableSupp);
        
        try{
            ResultSet tsup = conn.select("SELECT * FROM supplier");
            while(tsup.next()){
                String id_supp = tsup.getString("ID_SUPPLIER");
                String nama_supplier = tsup.getString("NAMA_SUPPLIER");
                String[] data = {id_supp, nama_supplier};
                TableSupp.addRow(data);
            }
        }
        catch(Exception e){
            
        }
    }

    public void tambah_produk(){
        String[][] data = {
            {"ID_SUPPLIER", LSupVal.getText()},
            {"PRODUK", TProduk.getText()},
            {"HARGA_KULAK",THargaKulak.getText()},
            {"HARGA",THarga.getText()},
            {"STOK",TStok.getText()}
        };
        try{
            boolean insert = conn.insert("produk", data);
            if(insert){
                Produk produk = new Produk();
                produk.setVisible(true);
                produk.setLocationRelativeTo(null);
                produk.setDefaultCloseOperation(Login.DO_NOTHING_ON_CLOSE);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Gagal menambah cek kembali!");
            } 
        }
        catch(Exception e){
            //System.out.println(e);
        }
    }
    public void update_produk(){
        String[][] data = {
            {"ID_SUPPLIER", LSupVal.getText()},
            {"PRODUK", TProduk.getText()},
            {"HARGA_KULAK",THargaKulak.getText()},
            {"HARGA",THarga.getText()},
            {"STOK",TStok.getText()}
        };
        String[] where = {"ID_PRODUK", Produk.id};
        try{
            boolean update = conn.update("produk", data, where);
            if(update){
                Produk.id = null;
                Produk produk = new Produk();
                produk.setVisible(true);
                produk.setLocationRelativeTo(null);
                produk.setDefaultCloseOperation(Login.DO_NOTHING_ON_CLOSE);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Gagal mengubah cek kembali!");
            } 
        }
        catch(Exception e){
            //System.out.println(e);
        }
    }
    public void dapat_produk(){
//        System.out.println(Produk.id);
        if(Produk.id != null){
            BTambah.setVisible(false);
            BBatal.setVisible(false);
            LStok.setVisible(false);
            TStok.setVisible(false);
            try{
                ResultSet tpro = conn.select("SELECT * FROM produk WHERE ID_PRODUK = "+Produk.id);
                if(tpro.next()){
                    LSupVal.setText(tpro.getString("ID_SUPPLIER"));
                    TProduk.setText(tpro.getString("PRODUK"));
                    THargaKulak.setText(tpro.getString("HARGA_KULAK"));
                    THarga.setText(tpro.getString("HARGA"));
                    TStok.setText(tpro.getString("STOK"));
                }
            }
            catch(Exception e){
                
            }
        }else{
            BUbah.setVisible(false);
        }
    }
